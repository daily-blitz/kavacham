import {
  type LoaderFunctionArgs,
  type ActionFunctionArgs,
} from '@shopify/remix-oxygen';
import {useLoaderData, type MetaFunction} from 'react-router';
import {getPaginationVariables, Analytics} from '@shopify/hydrogen';
import {SearchForm} from '~/components/SearchForm';
import {SearchResults} from '~/components/SearchResults';
import {
  type RegularSearchReturn,
  type PredictiveSearchReturn,
  getEmptyPredictiveSearchResult,
} from '~/lib/search';

export const meta: MetaFunction = () => {
  return [{title: `Search | Kavacham`}];
};

export async function loader({request, context}: LoaderFunctionArgs) {
  const url = new URL(request.url);
  const isPredictive = url.searchParams.has('predictive');
  const searchPromise: Promise<PredictiveSearchReturn | RegularSearchReturn> =
    isPredictive
      ? predictiveSearch({request, context})
      : regularSearch({request, context});

  searchPromise.catch((error: Error) => {
    console.error(error);
    return {term: '', result: null, error: error.message};
  });

  return await searchPromise;
}

/**
 * Renders the /search route
 */
export default function SearchPage() {
  const {type, term, result, error} = useLoaderData<typeof loader>();
  if (type === 'predictive') return null;

  return (
    <div className="container-custom py-4 sm:py-8 px-4 sm:px-6 lg:px-8">
      <div className="max-w-4xl mx-auto">
        <h1 className="text-2xl sm:text-3xl font-bold text-gray-900 mb-6 sm:mb-8 text-center">Search Products</h1>
        
        <div className="flex justify-center mb-8">
          <SearchForm>
            {({inputRef}) => (
              <div className="flex flex-col sm:flex-row gap-4 w-full max-w-md sm:max-w-lg">
                <input
                  defaultValue={term}
                  name="q"
                  placeholder="Search products..."
                  ref={inputRef}
                  type="search"
                  className="flex-1 px-4 py-3 border border-gray-300 rounded-lg focus:outline-none focus:ring-2 focus:ring-black focus:border-transparent"
                />
                <button 
                  type="submit"
                  className="bg-black text-white px-6 py-3 rounded-lg hover:bg-gray-800 transition-colors whitespace-nowrap"
                >
                  Search
                </button>
              </div>
            )}
          </SearchForm>
        </div>

        {error && (
          <div className="bg-red-50 border border-red-200 rounded-lg p-4 mb-8">
            <p className="text-red-800">{error}</p>
          </div>
        )}

        {term && result?.total ? (
          <div>
            <p className="text-gray-600 mb-6 text-center sm:text-left">
              Found {result.total} {result.total === 1 ? 'result' : 'results'} for "{term}"
            </p>
            <SearchResults result={result} term={term}>
              {({products}) => (
                <SearchResults.Products products={products} term={term} />
              )}
            </SearchResults>
          </div>
        ) : term ? (
          <div className="text-center py-8 sm:py-12">
            <p className="text-gray-600 text-sm sm:text-base">No products found for "{term}"</p>
          </div>
        ) : (
          <div className="text-center py-8 sm:py-12">
            <p className="text-gray-600 text-sm sm:text-base">Enter a search term to find products</p>
          </div>
        )}

        <Analytics.SearchView data={{searchTerm: term, searchResults: result}} />
      </div>
    </div>
  );
}

